<h1>What University of Nottingham graduates do after graduation</h1>
<p>Finding out what our graduates have gone on to do after leaving is very important to the Careers and Employability Service and to the University. Recent graduates are contacted to take part in the national Destinations of Leavers from Higher Education (DLHE) Survey which all universities take part in.</p>
<p>The information gained from this survey, is of key importance to the University in that it informs the League Tables and therefore affects our standing in those. It also provides data to our current and potential students about career paths of past graduates from their courses and it provides academics with employability figures for their courses.</p> <p>We collect this information in November-March of each year, approximately six months after graduation, and we encourage all graduates to tell us what they are doing in terms of employment or further study. We aim to celebrate the University`s successes through the achievements of our graduates but also continue to support those (for up to 3 years) who continue to need the support of the Careers and Employability Service.</p>
<p>Please use the links below to view the latest destinations figures for the University from the 2008/2009, 2009/2010 & 2010/2011 graduate DLHE surveys. The results have been broken down to each Academic School and individual Subject, as well as information about our graduate impact on the labour market.</p>
<h2>Message to all finalists</h2>
<p>The Destination of Leavers Survey is carried out every year by all universities, asking their own graduates what they are doing six months after graduation.&nbsp; The Higher Education Statistics Agency specify a target of an 80% response rate so it is very important that we can contact you, please ensure that the university has your correct contact details. The survey only takes a couple of minutes to complete.&nbsp;</p>
